package Ejemplo_Serializacion;

import java.io.Serializable;

/**
 *
 * @author larapresa
 */
public class Punto2D implements Serializable {

    private int x;
    private int y;

    public Punto2D(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return "x:" + x + ",y:" + y;
    }
}
